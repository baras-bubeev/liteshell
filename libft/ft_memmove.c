#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t i;

	if (!dst && !src)
		return (0);
	i = len - 1;
	if ((size_t)dst - (size_t)src < len)
	{
		while (len != 0)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			--i;
			--len;
		}
	}
	else
	{
		i = 0;
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dst);
}
