#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		j;
	char	*p;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	p = malloc(i + 1);
	if (p != 0)
	{
		while (j < i)
		{
			p[j] = s1[j];
			j++;
		}
		p[j] = '\0';
	}
	return (p);
}
